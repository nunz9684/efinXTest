
public class TextInfo 
{
	
	private String[] str;
	private int level;
	private int[] power;
	
	
	public String[] getStr() {
		return str;
	}
	public void setStr(String[] str) {
		this.str = str;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int[] getPower() {
		return power;
	}
	public void setPower(int[] power) {
		this.power = power;
	}
	
	
}
